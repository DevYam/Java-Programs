public class ToUpperCaseAndToLowerCase {
    public static void main(String[] args) {
        System.out.println("my name is divyam".toUpperCase());      // MY NAME IS DIVYAM
        System.out.println("MY NAME IS DIVYAM".toLowerCase());      // my name is divyam

        System.out.println("My name is Divyam".toLowerCase());      // my name is divyam
        System.out.println("My name is Divyam".toUpperCase());      // MY NAME IS DIVYAM
    }
}
